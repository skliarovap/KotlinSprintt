package com.kotlinsprint.lesson_3

fun main() {
    val fistName = "Татьяна"
    var lastName = "Андреева"
    val surname = "Сергеевна"
    var years = 20
    println("$lastName $fistName $surname, $years")

    lastName = "Сидорова"
    years = 22
    println("$lastName $fistName $surname, $years")
}